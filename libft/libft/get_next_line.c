#include "libft.h"

char	*get_next_line(int fd)
{
	char	buffer;
	char	*line;
	int		b;
	int		i;

	if (fd < 0)
		return (NULL);
	line = (char *)malloc(sizeof(char) * 2000000);
	if (!line)
		return (NULL);
	i = 0;
	b = read(fd, &buffer, 1);
	while (b > 0)
	{
		line[i++] = buffer;
		if (buffer == '\n')
			break ;
		b = read(fd, &buffer, 1);
	}
	line[i] = '\0';
	if (b <= 0 && i == 0)
		return (NULL);
	return (line);
}
